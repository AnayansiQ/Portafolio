print('+--------------------------------------------------+')
print("|Verifica si un numero es positivo, cero o negativo|")
print('+--------------------------------------------------+ \n')

Numero = float(input("Ingrese un numero: "))

print('\n')

if Numero >  0: 
    print("El numero es positivo")
   
elif Numero == 0:
    print("El numero es cero")
    
else: 
    print("El numero es negativo")